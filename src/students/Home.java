    public boolean check() {
        String newEmail = studentEmail.getText();
        String newPhone = studentName.getText();
        String oldEmail = model.getValueAt(rowIndex, 4).toString();
        String oldPhone = model.getValueAt(rowIndex, 5).toString();
        if (newEmail.equals(oldEmail) && newPhone.equals(oldPhone)) {
            return false;
        } else {
            if (!newEmail.equals(oldEmail)) {
                boolean x = sd.isEmailExist(newEmail);
                if (x) {
                    JOptionPane.showMessageDialog(null, "This Email Already Exists", "Warning", 2);
                }
                return x;
            }
            if (!newPhone.equals(oldPhone)) {
                boolean x = sd.isPhoneExist(newPhone);
                if (x) {
                    JOptionPane.showMessageDialog(null, "This Phone Already Exists", "Warning", 2);
                }
                return x;
            }
            return false;
        }
    }

