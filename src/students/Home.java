    private boolean isEmpty() {
        if (studentName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student's name is required");
            return false;
        }
        if (studentDofB.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Student's date of birth is required");
            return false;
        }
        if (studentDofB.getDate().compareTo(new Date()) > 0) {
            JOptionPane.showMessageDialog(this, "No student from the future are allowed");
            return false;
        }
        if (studentEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student's email address is required");
            return false;
        }
        if (!studentEmail.getText().matches("^.+@.+\\..+$")) {
            JOptionPane.showMessageDialog(this, "Invalid email address");
            return false;
        }
        if (studentPhone.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student's phone number is required");
            return false;
        }
        if (studentPhone.getText().length() >= 15) {
            JOptionPane.showMessageDialog(this, "Phone number is too long");
            return false;
        }
        if (studentFName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student's father name is required");
            return false;
        }
        if (studentMName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student's mother name is required");
            return false;
        }
        if (studentAddress1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Address line 1 is required");
            return false;
        }
        if (studentAddress2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Address line 2 is required");
            return false;
        }
        if (imgPath == null) {
            JOptionPane.showMessageDialog(this, "Please add your image");
            return false;
        }

        return true;
    }

